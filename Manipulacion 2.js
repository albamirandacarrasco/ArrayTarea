//Tarea de manipulacion 2 Combina los arrays
let nombres = ["Alba", "Pepe", "Luis"];
let edades = [1, 33, 27];

//concat
let personas = nombres.concat(edades);
console.log("Array combinado de nombres y edades:", personas);

//join
let cadenaPersonas = personas.join(", ");
console.log("Array con los elementos combinados:", cadenaPersonas);

